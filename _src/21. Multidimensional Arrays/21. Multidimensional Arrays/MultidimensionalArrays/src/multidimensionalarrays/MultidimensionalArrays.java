
package multidimensionalarrays;

public class MultidimensionalArrays {

    public static void main(String[] args) {
        /*
            ------------------------------------
               [0][0]   |  [0][1] |   [0][2]   |
            ------------------------------------
            ------------------------------------
               [1][0]   |  [1][1] |   [1][2]   |
            ------------------------------------
            ------------------------------------
               [2][0]   |  [2][1] |   [2][2]   |
            ------------------------------------
            ------------------------------------
               [3][0]   |  [3][1] |   [3][2]   |
            ------------------------------------        
        */
        
        int[][] tab = new int[4][3];
        
        tab[2][1] = 99;
        
        System.out.println(tab[2][1]);
        
        int[][] tab2 = 
        {
            {512, 125, -3},
            {45, 51, 124},
            {4124, 1234, -1556},
            {41, -55, -1212}
        };
        System.out.println(tab2[3][1]);
                
    }
    
}
